class Loops{
    public static void main(String[] args) {
        int i, j=0, k=0;
        for(i=0; i<5; i++){
            System.out.println("Hello");
        }
        System.out.println();
        
        while(j<5){
            System.out.println("Hello");
            j++;
        }
        System.out.println();
        
        do{
            System.out.println("Hello");
            k++;
        }while(k<5);
        
    }
}